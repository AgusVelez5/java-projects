package com.rajendarreddyj.basics.condition;

public class SimpleWhileLoopExample {

    public static void main(final String[] args) {

        int i = 0;
        int max = 5;

        System.out.println("Counting to " + max);

        while (i < max) {
            i++;
            System.out.println("i is : " + i);
        }

    }

}
