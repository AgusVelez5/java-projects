package com.rajendarreddyj.basics.operators;

public class ModuloOperatorExample {

    public static void main(final String[] args) {

        int i = 81;
        double d = 48;

        System.out.println(i + " mod 10 = " + (i % 10));
        System.out.println(d + " mod 10 = " + (d % 10));

    }

}
